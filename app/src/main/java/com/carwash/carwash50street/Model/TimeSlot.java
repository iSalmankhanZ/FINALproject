package com.carwash.carwash50street.Model;

public class TimeSlot {
    public String timea;
    public String timeb;
    public String timec;

    public TimeSlot() {
    }

    public TimeSlot(String timea) {
        this.timea = timea;
    }

    public String getTimea() {
        return timea;
    }

    public void setTimea(String timea) {
        this.timea = timea;
    }

    public String getTimeb() {
        return timeb;
    }

    public void setTimeb(String timeb) {
        this.timeb = timeb;
    }

    public String getTimec() {
        return timec;
    }

    public void setTimec(String timec) {
        this.timec = timec;
    }
}
